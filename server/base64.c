buffer_t base64_encode(buffer_t in, buffer_t out) {
	size_t padding, length = in.length;
	uint8_t * bin = in.bytes, * bout = out.bytes;
	
	while(length > 2) {
		bout[0] = bin[0] >> 2;
		bout[1] = ((bin[0] & ((1 << 2) - 1)) << 4) | (bin[1] >> 4);
		bout[2] = ((bin[1] & ((1 << 4) - 1)) << 2) | (bin[2] >> 6);
		bout[3] = bin[2] & ((1 << 6) - 1);
		length -= 3;
		bin += 3;
		bout += 4; }

	if(length == 2) {
		bout[0] = bin[0] >> 2;
		bout[1] = ((bin[0] & ((1 << 2) - 1)) << 4) | (bin[1] >> 4);
		bout[2] = ((bin[1] & ((1 << 4) - 1)) << 2);
		bout += 3;
		padding = 1; }

	else if(length == 1) {
		bout[0] = bin[0] >> 2;
		bout[1] = (bin[0] & ((1 << 2) - 1)) << 4;
		bout += 2;
		padding = 2; }

	out.length = bout - out.bytes;

	for(size_t i = 0; i < out.length; i++) {
		out.bytes[i] = base64_encode_table[out.bytes[i]]; }

	for(size_t i = 0; i < padding; i++) {
		out.bytes[out.length + i] = '='; }

	out.length += padding; 

	return out; }


